package com.gs.tour.shxt.login_record.model;

public class LoginRecord {
	Integer login_record_id;
	String login_record_name;
	String login_record_create_date;
	String login_record_account;
	public Integer getLogin_record_id() {
		return login_record_id;
	}
	public void setLogin_record_id(Integer login_record_id) {
		this.login_record_id = login_record_id;
	}
	public String getLogin_record_name() {
		return login_record_name;
	}
	public void setLogin_record_name(String login_record_name) {
		this.login_record_name = login_record_name;
	}
	public String getLogin_record_create_date() {
		return login_record_create_date;
	}
	public void setLogin_record_create_date(String login_record_create_date) {
		this.login_record_create_date = login_record_create_date;
	}
	public String getLogin_record_account() {
		return login_record_account;
	}
	public void setLogin_record_account(String login_record_account) {
		this.login_record_account = login_record_account;
	}
}
