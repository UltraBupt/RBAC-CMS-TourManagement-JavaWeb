package com.gs.tour.shxt.hotel.model;

public class Hotel {
	Integer hotel_id;
	String hotel_url;
	String hotel_name;
	String hotel_price;
	String hotel_address;
	String hotel_desc;
	Integer hotel_status;
	public Integer getHotel_id() {
		return hotel_id;
	}
	public void setHotel_id(Integer hotel_id) {
		this.hotel_id = hotel_id;
	}
	public String getHotel_url() {
		return hotel_url;
	}
	public void setHotel_url(String hotel_url) {
		this.hotel_url = hotel_url;
	}
	public String getHotel_name() {
		return hotel_name;
	}
	public void setHotel_name(String hotel_name) {
		this.hotel_name = hotel_name;
	}
	public String getHotel_price() {
		return hotel_price;
	}
	public void setHotel_price(String hotel_price) {
		this.hotel_price = hotel_price;
	}
	public String getHotel_address() {
		return hotel_address;
	}
	public void setHotel_address(String hotel_address) {
		this.hotel_address = hotel_address;
	}
	public String getHotel_desc() {
		return hotel_desc;
	}
	public void setHotel_desc(String hotel_desc) {
		this.hotel_desc = hotel_desc;
	}
	public Integer getHotel_status() {
		return hotel_status;
	}
	public void setHotel_status(Integer hotel_status) {
		this.hotel_status = hotel_status;
	}
}
