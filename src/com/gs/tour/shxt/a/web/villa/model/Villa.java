package com.gs.tour.shxt.a.web.villa.model;

public class Villa {
	Integer villa_id;
	String villa_url;
	String villa_name;
	String villa_price;
	String villa_address;
	String villa_desc;
	Integer villa_status;
	public Integer getVilla_id() {
		return villa_id;
	}
	public void setVilla_id(Integer villa_id) {
		this.villa_id = villa_id;
	}
	public String getVilla_url() {
		return villa_url;
	}
	public void setVilla_url(String villa_url) {
		this.villa_url = villa_url;
	}
	public String getVilla_name() {
		return villa_name;
	}
	public void setVilla_name(String villa_name) {
		this.villa_name = villa_name;
	}
	public String getVilla_price() {
		return villa_price;
	}
	public void setVilla_price(String villa_price) {
		this.villa_price = villa_price;
	}
	public String getVilla_address() {
		return villa_address;
	}
	public void setVilla_address(String villa_address) {
		this.villa_address = villa_address;
	}
	public String getVilla_desc() {
		return villa_desc;
	}
	public void setVilla_desc(String villa_desc) {
		this.villa_desc = villa_desc;
	}
	public Integer getVilla_status() {
		return villa_status;
	}
	public void setVilla_status(Integer villa_status) {
		this.villa_status = villa_status;
	}
}
