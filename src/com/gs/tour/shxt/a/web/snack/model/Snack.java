package com.gs.tour.shxt.a.web.snack.model;

public class Snack {
	Integer snack_id;
	String snack_url;
	String snack_name;
	String snack_price;
	String snack_address;
	String snack_desc;
	Integer snack_status;
	public Integer getSnack_id() {
		return snack_id;
	}
	public void setSnack_id(Integer snack_id) {
		this.snack_id = snack_id;
	}
	public String getSnack_url() {
		return snack_url;
	}
	public void setSnack_url(String snack_url) {
		this.snack_url = snack_url;
	}
	public String getSnack_name() {
		return snack_name;
	}
	public void setSnack_name(String snack_name) {
		this.snack_name = snack_name;
	}
	public String getSnack_price() {
		return snack_price;
	}
	public void setSnack_price(String snack_price) {
		this.snack_price = snack_price;
	}
	public String getSnack_address() {
		return snack_address;
	}
	public void setSnack_address(String snack_address) {
		this.snack_address = snack_address;
	}
	public String getSnack_desc() {
		return snack_desc;
	}
	public void setSnack_desc(String snack_desc) {
		this.snack_desc = snack_desc;
	}
	public Integer getSnack_status() {
		return snack_status;
	}
	public void setSnack_status(Integer snack_status) {
		this.snack_status = snack_status;
	}
}
