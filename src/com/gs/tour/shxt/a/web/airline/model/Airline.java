package com.gs.tour.shxt.a.web.airline.model;

public class Airline {
	Integer airline_id;
	String airline_url;
	String airline_from;
	String airline_to;
	String airline_price;
	String airline_time;
	String airline_point;
	Integer airline_status;
	public Integer getAirline_id() {
		return airline_id;
	}
	public void setAirline_id(Integer airline_id) {
		this.airline_id = airline_id;
	}
	public String getAirline_url() {
		return airline_url;
	}
	public void setAirline_url(String airline_url) {
		this.airline_url = airline_url;
	}
	public String getAirline_from() {
		return airline_from;
	}
	public void setAirline_from(String airline_from) {
		this.airline_from = airline_from;
	}
	public String getAirline_to() {
		return airline_to;
	}
	public void setAirline_to(String airline_to) {
		this.airline_to = airline_to;
	}
	public String getAirline_price() {
		return airline_price;
	}
	public void setAirline_price(String airline_price) {
		this.airline_price = airline_price;
	}
	public String getAirline_time() {
		return airline_time;
	}
	public void setAirline_time(String airline_time) {
		this.airline_time = airline_time;
	}
	public String getAirline_point() {
		return airline_point;
	}
	public void setAirline_point(String airline_point) {
		this.airline_point = airline_point;
	}
	public Integer getAirline_status() {
		return airline_status;
	}
	public void setAirline_status(Integer airline_status) {
		this.airline_status = airline_status;
	}
}
