package com.gs.tour.shxt.a.web.quick.model;

public class Quick {
	Integer quick_id;
	String quick_url;
	String quick_name;
	String quick_price;
	String quick_address;
	String quick_desc;
	Integer quick_status;
	public Integer getQuick_id() {
		return quick_id;
	}
	public void setQuick_id(Integer quick_id) {
		this.quick_id = quick_id;
	}
	public String getQuick_url() {
		return quick_url;
	}
	public void setQuick_url(String quick_url) {
		this.quick_url = quick_url;
	}
	public String getQuick_name() {
		return quick_name;
	}
	public void setQuick_name(String quick_name) {
		this.quick_name = quick_name;
	}
	public String getQuick_price() {
		return quick_price;
	}
	public void setQuick_price(String quick_price) {
		this.quick_price = quick_price;
	}
	public String getQuick_address() {
		return quick_address;
	}
	public void setQuick_address(String quick_address) {
		this.quick_address = quick_address;
	}
	public String getQuick_desc() {
		return quick_desc;
	}
	public void setQuick_desc(String quick_desc) {
		this.quick_desc = quick_desc;
	}
	public Integer getQuick_status() {
		return quick_status;
	}
	public void setQuick_status(Integer quick_status) {
		this.quick_status = quick_status;
	}
}
