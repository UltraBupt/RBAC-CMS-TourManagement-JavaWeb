package com.gs.tour.shxt.a.web.advertisement.model;

public class Advertisement {
	Integer advertisement_id;
	String advertisement_desc;
	String advertisement_name;
	String advertisement_benefit;
	public Integer getAdvertisement_id() {
		return advertisement_id;
	}
	public void setAdvertisement_id(Integer advertisement_id) {
		this.advertisement_id = advertisement_id;
	}
	public String getAdvertisement_desc() {
		return advertisement_desc;
	}
	public void setAdvertisement_desc(String advertisement_desc) {
		this.advertisement_desc = advertisement_desc;
	}
	public String getAdvertisement_name() {
		return advertisement_name;
	}
	public void setAdvertisement_name(String advertisement_name) {
		this.advertisement_name = advertisement_name;
	}
	public String getAdvertisement_benefit() {
		return advertisement_benefit;
	}
	public void setAdvertisement_benefit(String advertisement_benefit) {
		this.advertisement_benefit = advertisement_benefit;
	}
}
