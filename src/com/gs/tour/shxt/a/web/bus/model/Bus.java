package com.gs.tour.shxt.a.web.bus.model;

public class Bus {
	Integer bus_id;
	String bus_url;
	String bus_from;
	String bus_to;
	String bus_price;
	String bus_time;
	String bus_point;
	Integer bus_status;
	public Integer getBus_id() {
		return bus_id;
	}
	public void setBus_id(Integer bus_id) {
		this.bus_id = bus_id;
	}
	public String getBus_url() {
		return bus_url;
	}
	public void setBus_url(String bus_url) {
		this.bus_url = bus_url;
	}
	public String getBus_from() {
		return bus_from;
	}
	public void setBus_from(String bus_from) {
		this.bus_from = bus_from;
	}
	public String getBus_to() {
		return bus_to;
	}
	public void setBus_to(String bus_to) {
		this.bus_to = bus_to;
	}
	public String getBus_price() {
		return bus_price;
	}
	public void setBus_price(String bus_price) {
		this.bus_price = bus_price;
	}
	public String getBus_time() {
		return bus_time;
	}
	public void setBus_time(String bus_time) {
		this.bus_time = bus_time;
	}
	public String getBus_point() {
		return bus_point;
	}
	public void setBus_point(String bus_point) {
		this.bus_point = bus_point;
	}
	public Integer getBus_status() {
		return bus_status;
	}
	public void setBus_status(Integer bus_status) {
		this.bus_status = bus_status;
	}
}
