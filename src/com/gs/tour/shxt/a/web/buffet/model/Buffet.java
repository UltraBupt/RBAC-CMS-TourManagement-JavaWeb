package com.gs.tour.shxt.a.web.buffet.model;

public class Buffet {
	Integer buffet_id;
	String buffet_url;
	String buffet_name;
	String buffet_price;
	String buffet_address;
	String buffet_desc;
	Integer buffet_status;
	public Integer getBuffet_id() {
		return buffet_id;
	}
	public void setBuffet_id(Integer buffet_id) {
		this.buffet_id = buffet_id;
	}
	public String getBuffet_url() {
		return buffet_url;
	}
	public void setBuffet_url(String buffet_url) {
		this.buffet_url = buffet_url;
	}
	public String getBuffet_name() {
		return buffet_name;
	}
	public void setBuffet_name(String buffet_name) {
		this.buffet_name = buffet_name;
	}
	public String getBuffet_price() {
		return buffet_price;
	}
	public void setBuffet_price(String buffet_price) {
		this.buffet_price = buffet_price;
	}
	public String getBuffet_address() {
		return buffet_address;
	}
	public void setBuffet_address(String buffet_address) {
		this.buffet_address = buffet_address;
	}
	public String getBuffet_desc() {
		return buffet_desc;
	}
	public void setBuffet_desc(String buffet_desc) {
		this.buffet_desc = buffet_desc;
	}
	public Integer getBuffet_status() {
		return buffet_status;
	}
	public void setBuffet_status(Integer buffet_status) {
		this.buffet_status = buffet_status;
	}
}
