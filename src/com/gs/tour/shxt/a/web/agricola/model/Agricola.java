package com.gs.tour.shxt.a.web.agricola.model;

public class Agricola {
	Integer agricola_id;
	String agricola_url;
	String agricola_name;
	String agricola_price;
	String agricola_address;
	String agricola_desc;
	Integer agricola_status;
	public Integer getAgricola_id() {
		return agricola_id;
	}
	public void setAgricola_id(Integer agricola_id) {
		this.agricola_id = agricola_id;
	}
	public String getAgricola_url() {
		return agricola_url;
	}
	public void setAgricola_url(String agricola_url) {
		this.agricola_url = agricola_url;
	}
	public String getAgricola_name() {
		return agricola_name;
	}
	public void setAgricola_name(String agricola_name) {
		this.agricola_name = agricola_name;
	}
	public String getAgricola_price() {
		return agricola_price;
	}
	public void setAgricola_price(String agricola_price) {
		this.agricola_price = agricola_price;
	}
	public String getAgricola_address() {
		return agricola_address;
	}
	public void setAgricola_address(String agricola_address) {
		this.agricola_address = agricola_address;
	}
	public String getAgricola_desc() {
		return agricola_desc;
	}
	public void setAgricola_desc(String agricola_desc) {
		this.agricola_desc = agricola_desc;
	}
	public Integer getAgricola_status() {
		return agricola_status;
	}
	public void setAgricola_status(Integer agricola_status) {
		this.agricola_status = agricola_status;
	}
}
