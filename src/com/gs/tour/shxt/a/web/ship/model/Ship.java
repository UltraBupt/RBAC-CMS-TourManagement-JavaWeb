package com.gs.tour.shxt.a.web.ship.model;

public class Ship {
	Integer ship_id;
	String ship_url;
	String ship_from;
	String ship_to;
	String ship_price;
	String ship_time;
	String ship_point;
	Integer ship_status;
	public Integer getShip_id() {
		return ship_id;
	}
	public void setShip_id(Integer ship_id) {
		this.ship_id = ship_id;
	}
	public String getShip_url() {
		return ship_url;
	}
	public void setShip_url(String ship_url) {
		this.ship_url = ship_url;
	}
	public String getShip_from() {
		return ship_from;
	}
	public void setShip_from(String ship_from) {
		this.ship_from = ship_from;
	}
	public String getShip_to() {
		return ship_to;
	}
	public void setShip_to(String ship_to) {
		this.ship_to = ship_to;
	}
	public String getShip_price() {
		return ship_price;
	}
	public void setShip_price(String ship_price) {
		this.ship_price = ship_price;
	}
	public String getShip_time() {
		return ship_time;
	}
	public void setShip_time(String ship_time) {
		this.ship_time = ship_time;
	}
	public String getShip_point() {
		return ship_point;
	}
	public void setShip_point(String ship_point) {
		this.ship_point = ship_point;
	}
	public Integer getShip_status() {
		return ship_status;
	}
	public void setShip_status(Integer ship_status) {
		this.ship_status = ship_status;
	}
}
