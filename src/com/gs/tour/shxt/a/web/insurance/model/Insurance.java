package com.gs.tour.shxt.a.web.insurance.model;

public class Insurance {
	Integer insurance_id;
	String insurance_url;
	String insurance_price;
	String insurance_name;
	Integer insurance_status;
	String insurance_maxPrice;
	String insurance_desc;
	public Integer getInsurance_id() {
		return insurance_id;
	}
	public void setInsurance_id(Integer insurance_id) {
		this.insurance_id = insurance_id;
	}
	public String getInsurance_url() {
		return insurance_url;
	}
	public void setInsurance_url(String insurance_url) {
		this.insurance_url = insurance_url;
	}
	public String getInsurance_price() {
		return insurance_price;
	}
	public void setInsurance_price(String insurance_price) {
		this.insurance_price = insurance_price;
	}
	public String getInsurance_name() {
		return insurance_name;
	}
	public void setInsurance_name(String insurance_name) {
		this.insurance_name = insurance_name;
	}
	public Integer getInsurance_status() {
		return insurance_status;
	}
	public void setInsurance_status(Integer insurance_status) {
		this.insurance_status = insurance_status;
	}
	public String getInsurance_maxPrice() {
		return insurance_maxPrice;
	}
	public void setInsurance_maxPrice(String insurance_maxPrice) {
		this.insurance_maxPrice = insurance_maxPrice;
	}
	public String getInsurance_desc() {
		return insurance_desc;
	}
	public void setInsurance_desc(String insurance_desc) {
		this.insurance_desc = insurance_desc;
	}
}
