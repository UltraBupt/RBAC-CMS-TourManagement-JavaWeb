package com.gs.tour.shxt.a.web.tour.model;

public class OutboundTourism {
	Integer outboundTourism_id;
	String outboundTourism_url;
	String outboundTourism_desc;
	String outboundTourism_name;
	public Integer getOutboundTourism_id() {
		return outboundTourism_id;
	}
	public void setOutboundTourism_id(Integer outboundTourism_id) {
		this.outboundTourism_id = outboundTourism_id;
	}
	public String getOutboundTourism_url() {
		return outboundTourism_url;
	}
	public void setOutboundTourism_url(String outboundTourism_url) {
		this.outboundTourism_url = outboundTourism_url;
	}
	public String getOutboundTourism_desc() {
		return outboundTourism_desc;
	}
	public void setOutboundTourism_desc(String outboundTourism_desc) {
		this.outboundTourism_desc = outboundTourism_desc;
	}
	public String getOutboundTourism_name() {
		return outboundTourism_name;
	}
	public void setOutboundTourism_name(String outboundTourism_name) {
		this.outboundTourism_name = outboundTourism_name;
	}
}
