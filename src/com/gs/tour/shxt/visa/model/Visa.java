package com.gs.tour.shxt.visa.model;

public class Visa {
	Integer visa_id;
	String visa_address;
	String visa_date;
	String visa_price;
	String visa_number;
	String visa_day;
	String visa_useful;
	Integer visa_status;
	String visa_url;
	public String getVisa_url() {
		return visa_url;
	}
	public void setVisa_url(String visa_url) {
		this.visa_url = visa_url;
	}
	public Integer getVisa_id() {
		return visa_id;
	}
	public void setVisa_id(Integer visa_id) {
		this.visa_id = visa_id;
	}
	public String getVisa_address() {
		return visa_address;
	}
	public void setVisa_address(String visa_address) {
		this.visa_address = visa_address;
	}
	public String getVisa_date() {
		return visa_date;
	}
	public void setVisa_date(String visa_date) {
		this.visa_date = visa_date;
	}
	public String getVisa_price() {
		return visa_price;
	}
	public void setVisa_price(String visa_price) {
		this.visa_price = visa_price;
	}
	public String getVisa_number() {
		return visa_number;
	}
	public void setVisa_number(String visa_number) {
		this.visa_number = visa_number;
	}
	public String getVisa_day() {
		return visa_day;
	}
	public void setVisa_day(String visa_day) {
		this.visa_day = visa_day;
	}
	public String getVisa_useful() {
		return visa_useful;
	}
	public void setVisa_useful(String visa_useful) {
		this.visa_useful = visa_useful;
	}
	public Integer getVisa_status() {
		return visa_status;
	}
	public void setVisa_status(Integer visa_status) {
		this.visa_status = visa_status;
	}
}
