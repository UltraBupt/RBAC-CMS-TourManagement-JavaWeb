package com.gs.tour.shxt.holiday.model;

public class Holiday {
	Integer holiday_id;
	String holiday_url;
	String holiday_name;
	String holiday_price;
	String holiday_address;
	String holiday_desc;
	Integer holiday_status;
	public Integer getHoliday_id() {
		return holiday_id;
	}
	public void setHoliday_id(Integer holiday_id) {
		this.holiday_id = holiday_id;
	}
	public String getHoliday_url() {
		return holiday_url;
	}
	public void setHoliday_url(String holiday_url) {
		this.holiday_url = holiday_url;
	}
	public String getHoliday_name() {
		return holiday_name;
	}
	public void setHoliday_name(String holiday_name) {
		this.holiday_name = holiday_name;
	}
	public String getHoliday_price() {
		return holiday_price;
	}
	public void setHoliday_price(String holiday_price) {
		this.holiday_price = holiday_price;
	}
	public String getHoliday_address() {
		return holiday_address;
	}
	public void setHoliday_address(String holiday_address) {
		this.holiday_address = holiday_address;
	}
	public String getHoliday_desc() {
		return holiday_desc;
	}
	public void setHoliday_desc(String holiday_desc) {
		this.holiday_desc = holiday_desc;
	}
	public Integer getHoliday_status() {
		return holiday_status;
	}
	public void setHoliday_status(Integer holiday_status) {
		this.holiday_status = holiday_status;
	}
}
