package com.gs.tour.shxt.apartment.model;

public class Apartment {
	Integer apartment_id;
	String apartment_url;
	String apartment_name;
	String apartment_price;
	String apartment_address;
	String apartment_desc;
	Integer apartment_status;
	public Integer getApartment_id() {
		return apartment_id;
	}
	public void setApartment_id(Integer apartment_id) {
		this.apartment_id = apartment_id;
	}
	public String getApartment_url() {
		return apartment_url;
	}
	public void setApartment_url(String apartment_url) {
		this.apartment_url = apartment_url;
	}
	public String getApartment_name() {
		return apartment_name;
	}
	public void setApartment_name(String apartment_name) {
		this.apartment_name = apartment_name;
	}
	public String getApartment_price() {
		return apartment_price;
	}
	public void setApartment_price(String apartment_price) {
		this.apartment_price = apartment_price;
	}
	public String getApartment_address() {
		return apartment_address;
	}
	public void setApartment_address(String apartment_address) {
		this.apartment_address = apartment_address;
	}
	public String getApartment_desc() {
		return apartment_desc;
	}
	public void setApartment_desc(String apartment_desc) {
		this.apartment_desc = apartment_desc;
	}
	public Integer getApartment_status() {
		return apartment_status;
	}
	public void setApartment_status(Integer apartment_status) {
		this.apartment_status = apartment_status;
	}
}
