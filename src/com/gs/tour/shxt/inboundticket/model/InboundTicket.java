package com.gs.tour.shxt.inboundticket.model;

public class InboundTicket {
	Integer inboundTicket_id;
	String inboundTicket_name;
	String inboundTicket_desc;
	String inboundTicket_url;
	public String getInboundTicket_url() {
		return inboundTicket_url;
	}
	public void setInboundTicket_url(String inboundTicket_url) {
		this.inboundTicket_url = inboundTicket_url;
	}
	String inboundTicket_price;
	public Integer getInboundTicket_id() {
		return inboundTicket_id;
	}
	public void setInboundTicket_id(Integer inboundTicket_id) {
		this.inboundTicket_id = inboundTicket_id;
	}
	public String getInboundTicket_name() {
		return inboundTicket_name;
	}
	public void setInboundTicket_name(String inboundTicket_name) {
		this.inboundTicket_name = inboundTicket_name;
	}
	public String getInboundTicket_desc() {
		return inboundTicket_desc;
	}
	public void setInboundTicket_desc(String inboundTicket_desc) {
		this.inboundTicket_desc = inboundTicket_desc;
	}
	public String getInboundTicket_price() {
		return inboundTicket_price;
	}
	public void setInboundTicket_price(String inboundTicket_price) {
		this.inboundTicket_price = inboundTicket_price;
	}
}
