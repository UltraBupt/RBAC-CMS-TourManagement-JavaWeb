package com.gs.tour.shxt.bill.model;

public class Bill {
	Integer bill_id;
	String bill_type;
	String bill_code;
	String bill_thing;
	String bill_memberAccount;
	String bill_memberPhone;
	String bill_price;
	String bill_number;
	double bill_totalPrice;
	Integer bill_status;
	String bill_createDate;
	public Integer getBill_id() {
		return bill_id;
	}
	public void setBill_id(Integer bill_id) {
		this.bill_id = bill_id;
	}
	public String getBill_type() {
		return bill_type;
	}
	public void setBill_type(String bill_type) {
		this.bill_type = bill_type;
	}
	public String getBill_code() {
		return bill_code;
	}
	public void setBill_code(String bill_code) {
		this.bill_code = bill_code;
	}
	public String getBill_thing() {
		return bill_thing;
	}
	public void setBill_thing(String bill_thing) {
		this.bill_thing = bill_thing;
	}
	public String getBill_memberAccount() {
		return bill_memberAccount;
	}
	public void setBill_memberAccount(String bill_memberAccount) {
		this.bill_memberAccount = bill_memberAccount;
	}
	public String getBill_memberPhone() {
		return bill_memberPhone;
	}
	public void setBill_memberPhone(String bill_memberPhone) {
		this.bill_memberPhone = bill_memberPhone;
	}
	public String getBill_price() {
		return bill_price;
	}
	public void setBill_price(String bill_price) {
		this.bill_price = bill_price;
	}
	public String getBill_number() {
		return bill_number;
	}
	public void setBill_number(String bill_number) {
		this.bill_number = bill_number;
	}
	
	public double getBill_totalPrice() {
		return bill_totalPrice;
	}
	public void setBill_totalPrice(double bill_totalPrice) {
		this.bill_totalPrice = bill_totalPrice;
	}
	public Integer getBill_status() {
		return bill_status;
	}
	public void setBill_status(Integer bill_status) {
		this.bill_status = bill_status;
	}
	public String getBill_createDate() {
		return bill_createDate;
	}
	public void setBill_createDate(String bill_createDate) {
		this.bill_createDate = bill_createDate;
	}
}
