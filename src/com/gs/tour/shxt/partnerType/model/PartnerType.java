package com.gs.tour.shxt.partnerType.model;

public class PartnerType {
	Integer partnerType_id;
	String partnerType_name;
	Integer partnerType_status;
	String partnerType_desc;
	public Integer getPartnerType_id() {
		return partnerType_id;
	}
	public void setPartnerType_id(Integer partnerType_id) {
		this.partnerType_id = partnerType_id;
	}
	public String getPartnerType_name() {
		return partnerType_name;
	}
	public void setPartnerType_name(String partnerType_name) {
		this.partnerType_name = partnerType_name;
	}
	public Integer getPartnerType_status() {
		return partnerType_status;
	}
	public void setPartnerType_status(Integer partnerType_status) {
		this.partnerType_status = partnerType_status;
	}
	public String getPartnerType_desc() {
		return partnerType_desc;
	}
	public void setPartnerType_desc(String partnerType_desc) {
		this.partnerType_desc = partnerType_desc;
	}
}
