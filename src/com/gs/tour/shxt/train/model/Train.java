package com.gs.tour.shxt.train.model;

public class Train {
	Integer train_id;
	String train_url;
	String train_from;
	String train_to;
	String train_price;
	String train_time;
	String train_point;
	Integer train_status;
	public Integer getTrain_id() {
		return train_id;
	}
	public void setTrain_id(Integer train_id) {
		this.train_id = train_id;
	}
	public String getTrain_url() {
		return train_url;
	}
	public void setTrain_url(String train_url) {
		this.train_url = train_url;
	}
	public String getTrain_from() {
		return train_from;
	}
	public void setTrain_from(String train_from) {
		this.train_from = train_from;
	}
	public String getTrain_to() {
		return train_to;
	}
	public void setTrain_to(String train_to) {
		this.train_to = train_to;
	}
	public String getTrain_price() {
		return train_price;
	}
	public void setTrain_price(String train_price) {
		this.train_price = train_price;
	}
	public String getTrain_time() {
		return train_time;
	}
	public void setTrain_time(String train_time) {
		this.train_time = train_time;
	}
	public String getTrain_point() {
		return train_point;
	}
	public void setTrain_point(String train_point) {
		this.train_point = train_point;
	}
	public Integer getTrain_status() {
		return train_status;
	}
	public void setTrain_status(Integer train_status) {
		this.train_status = train_status;
	}
}
