package com.gs.tour.shxt.branch.model;

public class Branch {
	Integer branch_id;
	Integer branch_status;
	String branch_name;
	String branch_desc;
	public Integer getBranch_id() {
		return branch_id;
	}
	public void setBranch_id(Integer branch_id) {
		this.branch_id = branch_id;
	}
	public Integer getBranch_status() {
		return branch_status;
	}
	public void setBranch_status(Integer branch_status) {
		this.branch_status = branch_status;
	}
	public String getBranch_name() {
		return branch_name;
	}
	public void setBranch_name(String branch_name) {
		this.branch_name = branch_name;
	}
	public String getBranch_desc() {
		return branch_desc;
	}
	public void setBranch_desc(String branch_desc) {
		this.branch_desc = branch_desc;
	}
	
}
