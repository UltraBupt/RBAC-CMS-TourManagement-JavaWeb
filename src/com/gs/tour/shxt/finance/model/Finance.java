package com.gs.tour.shxt.finance.model;

public class Finance {
	Integer finance_id;
	String finance_url;
	String finance_name;
	String finance_price;
	String finance_number;
	String finance_desc;
	Integer finance_status;
	public Integer getFinance_id() {
		return finance_id;
	}
	public void setFinance_id(Integer finance_id) {
		this.finance_id = finance_id;
	}
	public String getFinance_url() {
		return finance_url;
	}
	public void setFinance_url(String finance_url) {
		this.finance_url = finance_url;
	}
	public String getFinance_name() {
		return finance_name;
	}
	public void setFinance_name(String finance_name) {
		this.finance_name = finance_name;
	}
	public String getFinance_price() {
		return finance_price;
	}
	public void setFinance_price(String finance_price) {
		this.finance_price = finance_price;
	}
	public String getFinance_number() {
		return finance_number;
	}
	public void setFinance_number(String finance_number) {
		this.finance_number = finance_number;
	}
	public String getFinance_desc() {
		return finance_desc;
	}
	public void setFinance_desc(String finance_desc) {
		this.finance_desc = finance_desc;
	}
	public Integer getFinance_status() {
		return finance_status;
	}
	public void setFinance_status(Integer finance_status) {
		this.finance_status = finance_status;
	}
}
