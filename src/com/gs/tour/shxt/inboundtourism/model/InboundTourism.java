package com.gs.tour.shxt.inboundtourism.model;

public class InboundTourism {
	Integer inboundTourism_id;
	String inboundTourism_url;
	String inboundTourism_desc;
	String inboundTourism_name;
	public Integer getInboundTourism_id() {
		return inboundTourism_id;
	}
	public void setInboundTourism_id(Integer inboundTourism_id) {
		this.inboundTourism_id = inboundTourism_id;
	}
	public String getInboundTourism_url() {
		return inboundTourism_url;
	}
	public void setInboundTourism_url(String inboundTourism_url) {
		this.inboundTourism_url = inboundTourism_url;
	}
	public String getInboundTourism_desc() {
		return inboundTourism_desc;
	}
	public void setInboundTourism_desc(String inboundTourism_desc) {
		this.inboundTourism_desc = inboundTourism_desc;
	}
	public String getInboundTourism_name() {
		return inboundTourism_name;
	}
	public void setInboundTourism_name(String inboundTourism_name) {
		this.inboundTourism_name = inboundTourism_name;
	}
}
