package com.gs.tour.shxt.recruit.model;

public class Recruit {
	Integer recruit_id;
	String recruit_company;
	String recruit_position;
	String recruit_salary;
	String recruit_address;
	String recruit_desc;
	String recruit_benefit;
	String recruit_date;
	Integer recruit_status;
	String recruit_email;
	public Integer getRecruit_id() {
		return recruit_id;
	}
	public void setRecruit_id(Integer recruit_id) {
		this.recruit_id = recruit_id;
	}
	public String getRecruit_company() {
		return recruit_company;
	}
	public void setRecruit_company(String recruit_company) {
		this.recruit_company = recruit_company;
	}
	public String getRecruit_position() {
		return recruit_position;
	}
	public void setRecruit_position(String recruit_position) {
		this.recruit_position = recruit_position;
	}
	public String getRecruit_salary() {
		return recruit_salary;
	}
	public void setRecruit_salary(String recruit_salary) {
		this.recruit_salary = recruit_salary;
	}
	public String getRecruit_address() {
		return recruit_address;
	}
	public void setRecruit_address(String recruit_address) {
		this.recruit_address = recruit_address;
	}
	public String getRecruit_desc() {
		return recruit_desc;
	}
	public void setRecruit_desc(String recruit_desc) {
		this.recruit_desc = recruit_desc;
	}
	public String getRecruit_benefit() {
		return recruit_benefit;
	}
	public void setRecruit_benefit(String recruit_benefit) {
		this.recruit_benefit = recruit_benefit;
	}
	public String getRecruit_date() {
		return recruit_date;
	}
	public void setRecruit_date(String recruit_date) {
		this.recruit_date = recruit_date;
	}
	public Integer getRecruit_status() {
		return recruit_status;
	}
	public void setRecruit_status(Integer recruit_status) {
		this.recruit_status = recruit_status;
	}
	public String getRecruit_email() {
		return recruit_email;
	}
	public void setRecruit_email(String recruit_email) {
		this.recruit_email = recruit_email;
	}
}
